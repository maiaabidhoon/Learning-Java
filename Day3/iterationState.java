class IterationState {

    public static void main(String[] args) {

        // For Loop

        for (int i = 1; i <= 10; i++) {
            System.out.println("For Loops: " + i);
        }

        // While Loop

        int i = 1;
        while (i <= 10) {
            System.out.println("While Loops: " + i);
            i++;
        }

        // Do-While Loop
        
        int j = 1;

        do {
            System.out.println("Do-While Loops: " + j);
            j++;
        } while (j <= 10);

    }

    
}