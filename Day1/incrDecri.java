public class incrDecri {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("The value of a is: " + a);
        a++;
        System.out.println("The value of a after incrementing is: " + a);
        a--;
        System.out.println("The value of a after decrementing is: " + a);
        ++a;
        System.out.println("The value of a after pre-incrementing is: " + a);
    }
}
